age = int(input("how old are you - "))
#input
if age <= "2" :
    print("you are a baby")
elif "2" <= age <= "4" :
    print("you are a toddler")
elif "4" <= age <= "13" :
    print("you are a kid")
elif "13" <= age <= "20" :
    print("you are a teen")
elif "20" <= age <= "65" :
    print("you are an adult")
elif "65" <= age <= "100" :
    print("you are an elder")
