def add():
    x = int(input(" x = ").strip())
    y = int(input(" y = ").strip())
    z = x + y
    print(f"{x} + {y} = {z}")

def sub():
    x2 = int(input(" x2 = ").strip())
    y2 = int(input(" y2 =  ").strip())
    z2 = x2 - y2
    print(f"{x2} - {y2} = {z2} ")
